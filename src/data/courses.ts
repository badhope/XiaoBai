// 构建课程库索引
export const courseLibrary: CourseLibrary = {
  courses,
  index: {
    byId: courses.reduce((acc, course) => {
      acc[course.id] = course;
      return acc;
    }, {} as Record<string, Course>),
    byCode: courses.reduce((acc, course) => {
      acc[course.code] = course;
      return acc;
    }, {} as Record<string, Course>),
    byTag: courses.reduce((acc, course) => {
      course.tags.forEach((tag) => {
        const normalizedTag = tag.toLowerCase();
        if (!acc[normalizedTag]) {
          acc[normalizedTag] = [];
        }
        acc[normalizedTag].push(course);
      });
      return acc;
    }, {} as Record<string, Course[]>),
  },
};
